#include "libft.h"

int		ft_is_prime(int nb)
{
	int i;
	int u;

	i = 2;
	u = 0;
	while (i < nb)
	{
		if (nb % i == 0)
			u++;
		i++;
	}
	if (u == 0)
		return (1);
	return (0);
}
