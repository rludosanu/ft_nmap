#include "libft.h"

int		ft_strisdigit(char *str)
{
	int		i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= '0' && str[i] <= '9')
			;
		else
			return (0);
		i++;
	}
	return (1);
}
