#include "ft_printf.h"

int		intlen(int nbr)
{
	int		len;

	len = 0;
	if (nbr < 0)
	{
		nbr = nbr * -1;
		len++;
	}
	while (nbr >= 10)
	{
		nbr = nbr / 10;
		len++;
	}
	if (nbr >= 0 && nbr < 10)
		len++;
	return (len);
}
